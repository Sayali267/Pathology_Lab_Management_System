                <h4 class="card-title">Overview</h4>
                <p>A complete cholesterol test — also called a lipid panel or lipid profile — is a blood test that can measure the amount of cholesterol and triglycerides in your blood.
                </p>
                <p>
                A cholesterol test can help determine your risk of the buildup of plaques in your arteries that can lead to narrowed or blocked arteries throughout your body (atherosclerosis).
                </p>

                <p>A cholesterol test is an important tool. High cholesterol levels often are a significant risk factor for coronary artery disease.
                </p>

                <h4 class="card-title">Why it's done?</h4>
                <p>High cholesterol usually causes no signs or symptoms. A complete cholesterol test is done to determine whether your cholesterol is high and estimate your risk of developing heart attacks and other forms of heart disease and diseases of the blood vessels.</p>
                <p>A complete cholesterol test includes the calculation of four types of fats (lipids) in your blood:
                    <ul>
                        <li><b>Total cholesterol.</b> This is a sum of your blood's cholesterol content.</li>
                        <li><b>High-density lipoprotein (HDL) cholesterol.</b> This is called the "good" cholesterol because it helps carry away LDL cholesterol, thus keeping arteries open and your blood flowing more freely.</li>
                        <li><b>Low-density lipoprotein (LDL) cholesterol.</b> This is called the "bad" cholesterol. Too much of it in your blood causes the buildup of fatty deposits (plaques) in your arteries (atherosclerosis), which reduces blood flow. These plaques sometimes rupture and can lead to a heart attack or stroke.</li>
                        <li><b>Triglycerides.</b> Triglycerides are a type of fat in the blood. When you eat, your body converts calories it doesn't need into triglycerides, which are stored in fat cells. High triglyceride levels are associated with several factors, including being overweight, eating too many sweets or drinking too much alcohol, smoking, being sedentary, or having diabetes with elevated blood sugar levels.</li>
                    </ul>
                </p>
                
                <h4 class="card-title">Who should get a cholesterol test?</h4>
                <p>Adults at average risk of developing coronary artery disease should have their cholesterol checked every five years, beginning at age 18.</p>
                <p>More-frequent testing might be needed if your initial test results were abnormal or if you already have coronary artery disease, you're taking cholesterol-lowering medications, or you're at higher risk of coronary artery disease because you:</p>
                    <ul>
                        <li>Have a family history of high cholesterol or heart attacks</li>
                        <li>Are overweight</li>
                        <li>Are physically inactive</li>
                        <li>Have diabetes</li>
                        <li>Eat an unhealthy diet</li>
                        <li>Smoke cigarettes</li>
                        <li>Are a man older than 45 or a woman older than 55</li>
                    </ul>
                <p>People with a history of heart attacks or strokes require regular cholesterol testing to monitor the effectiveness of their treatments.</p>

                <h4 class="card-title">How you prepare?</h4>
                <p>Generally you're required to fast, consuming no food or liquids other than water, for nine to 12 hours before the test. Some cholesterol tests don't require fasting, so follow your doctor's instructions.</p>

                <h4 class="card-title">What you can expect?</h4>
                    <h5 class="card-title">During the procedure</h5>
                    <p>A cholesterol test is a blood test, usually done in the morning since you'll need to fast for the most accurate results. Blood is drawn from a vein, usually from your arm.</p>
                    <p>Before the needle is inserted, the puncture site is cleaned with antiseptic and an elastic band is wrapped around your upper arm. This causes the veins in your arm to fill with blood.</p>
                    <p>After the needle is inserted, a small amount of blood is collected into a vial or syringe. The band is then removed to restore circulation, and blood continues to flow into the vial. Once enough blood is collected, the needle is removed and the puncture site is covered with a bandage.</p>
                    <p>The procedure will likely take a couple of minutes. It's relatively painless.</p>

                    <h5 class="card-title">After the procedure</h5>
                    <p>There are no precautions you need to take after your cholesterol test. You should be able to drive yourself home and do all your normal activities. You might want to bring a snack to eat after your cholesterol test is done, if you've been fasting.</p>

